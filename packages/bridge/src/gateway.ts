// --- Skills → CLAUDE.md writer ---

const MLB_START = '<!-- MLB_SKILLS_START -->';
const MLB_END = '<!-- MLB_SKILLS_END -->';

/**
 * Write skill documents into the CC work directory's .claude/CLAUDE.md.
 * Claude Code auto-reads CLAUDE.md at session start — no system prompt injection needed.
 *
 * Strategy:
 * - Read all src/skills/*.md files
 * - Check if .claude/CLAUDE.md exists; if it does, check for MLB marker
 * - If marker exists, replace the MLB section; if not, append
 * - If file doesn't exist, create it with the MLB section
 */
function writeSkillsToCLAUDEMd(workDir: string): void {
  try {
    const thisDir = dirname(fileURLToPath(import.meta.url));
    const skillsDir = join(thisDir, 'skills');
    if (!existsSync(skillsDir)) return;

    const files = readdirSync(skillsDir).filter(f => f.endsWith('.md')).sort();
    if (files.length === 0) return;

    const sections: string[] = [];
    for (const file of files) {
      const content = readFileSync(join(skillsDir, file), 'utf-8');
      const stripped = content.replace(/^---[\s\S]*?---\n*/m, '').trim();
      if (stripped) sections.push(stripped);
    }

    const skillsBlock = [
      MLB_START,
      '# Feishu/Lark MCP Tool Usage Guide',
      '',
      '> Auto-generated by Matrix Lark Bridge. Do not edit this section manually.',
      '',
      ...sections,
      MLB_END,
    ].join('\n');

    const claudeDir = join(workDir, '.claude');
    const claudeMdPath = join(claudeDir, 'CLAUDE.md');

    mkdirSync(claudeDir, { recursive: true });

    if (existsSync(claudeMdPath)) {
      let existing = readFileSync(claudeMdPath, 'utf-8');
      const startIdx = existing.indexOf(MLB_START);
      const endIdx = existing.indexOf(MLB_END);
      if (startIdx !== -1 && endIdx !== -1) {
        // Replace existing MLB section
        existing = existing.substring(0, startIdx) + skillsBlock + existing.substring(endIdx + MLB_END.length);
      } else {
        // Append MLB section
        existing = existing.trimEnd() + '\n\n' + skillsBlock + '\n';
      }
      writeFileSync(claudeMdPath, existing);
    } else {
      writeFileSync(claudeMdPath, skillsBlock + '\n');
    }

    console.log(`[gateway] wrote ${files.length} skill docs to ${claudeMdPath}`);
  } catch (err) {
    console.warn(`[gateway] failed to write CLAUDE.md: ${(err as Error).message}`);
    // Non-fatal — bridge continues without skill docs in CLAUDE.md
  }
}


  // --- Message handling ---

  private async handleMessage(content: string, images: ImageAttachment[], chatId: string, messageId: string, senderInfo?: SenderInfo): Promise<void> {
    // Store sender info for user context injection
    if (senderInfo) {
      this.lastSenderInfo = senderInfo;
    }
    console.log(`[gateway] handleMessage: content="${content.substring(0, 50)}" chatId=${chatId} sender=${senderInfo?.openId ?? 'unknown'}`);

    // Check for slash commands
    if (content.startsWith('/')) {
      const parts = content.trim().split(/\s+/);
      const cmd = parts[0].toLowerCase();
      const handled = await this.handleCommand(cmd, parts.slice(1), chatId);
      if (handled) return;
    }

    // Handle pending permission text responses
    const lowerContent = content.toLowerCase().trim();
    if (lowerContent === 'allow' || lowerContent === 'deny' || lowerContent === 'y' || lowerContent === 'n') {
      const pending = this.findPendingPermission();
      if (pending) {
        const behavior = (lowerContent === 'allow' || lowerContent === 'y') ? 'allow' as const : 'deny' as const;
        pending.entry.resolve({ behavior });
        this.pendingPermissions.delete(pending.requestId);
        await this.feishu.sendCard(chatId, behavior === 'allow'
          ? buildNoticeCard('已允许', undefined, 'green')
          : buildNoticeCard('已拒绝', undefined, 'red'));
        return;
      }
    }

    // Handle pending AskUserQuestion text response
    if (this.pendingAnswer) {
      const askMsgId = this.pendingAnswer.msgId;
      this.pendingAnswer.resolve(content);
      this.pendingAnswer = null;
      // Update the original ask card to show the answer
      const resultCard = buildNoticeCard('已回答', `✅ ${content}`, 'green');
      if (askMsgId) await this.feishu.updateCard(askMsgId, resultCard);
      else await this.feishu.sendCard(chatId, resultCard);
      return;
    }

    // Handle pending OAuth code paste
    if (this.pendingAuthCode && content.trim().length > 10 && content.trim().length < 200 && !content.startsWith('/')) {
      let code = content.trim();
      // Extract code from URL if user pasted the full callback URL
      try {
        if (code.startsWith('http')) {
          const url = new URL(code);
          const codeParam = url.searchParams.get('code');
          if (codeParam) code = codeParam;
        }
      } catch { /* not a URL, use as-is */ }
      this.pendingAuthCode.resolve(code);
      this.pendingAuthCode = null;
      await this.feishu.sendCard(chatId, buildNoticeCard('收到授权码', '正在验证…', 'blue'));
      return;
    }

    // Queue mechanism: enqueue message for sequential processing
    if (this.processing) {
      if (this.maxQueue > 0 && this.messageQueue.length >= this.maxQueue) {
        await this.feishu.sendCard(chatId, buildNoticeCard(
          '队列已满',
          `当前有 ${this.messageQueue.length} 条消息排队中，请稍后再试。`,
          'orange',
        ));
        return;
      }
      const pos = this.messageQueue.length + 1;
      const queueCardId = await this.feishu.sendCard(chatId, buildNoticeCard(
        '已排队',
        `前方还有 ${pos} 条消息，请稍候…`,
        'blue',
      ));
      this.messageQueue.push({ content, images, chatId, messageId, senderInfo, enqueuedAt: Date.now(), cardMsgId: queueCardId || undefined });
      console.log(`[gateway] message queued (position ${pos}/${this.maxQueue})`);
      return;
    }

    await this.processMessage(content, images, chatId, messageId, senderInfo);
  }

  /** Process a single message (called directly or from queue) */
  private async processMessage(
    content: string, images: ImageAttachment[], chatId: string, messageId: string,
    senderInfo?: SenderInfo, reuseCardId?: string,
  ): Promise<void> {
    this.processing = true;
    if (senderInfo) this.lastSenderInfo = senderInfo;

    const stopTyping = this.feishu.startTyping(messageId);

    try {
      this.store.addHistory('user', content);
      this.store.resetTurn();
      const thinkingCard = buildThinkingCard(content, this.botName);

      let cardMsgId: string | undefined;
      if (reuseCardId) {
        // Update the "已排队" card to thinking card
        await this.feishu.updateCard(reuseCardId, thinkingCard);
        cardMsgId = reuseCardId;
      } else {
        cardMsgId = await this.feishu.sendCard(chatId, thinkingCard) || undefined;
      }
      if (cardMsgId) {
        this.store.setMessageId(cardMsgId);
      }

      await this.ensureSession();
      const now = new Date();
      const timePrefix = `[${now.toLocaleString('zh-CN', { hour12: false, timeZone: 'Asia/Shanghai' })}] `;
      const contextPrefix = this.buildUserContext(chatId);
      await this.requireSession().send(contextPrefix + timePrefix + content, images.length > 0 ? images : undefined);

      let retries = 0;
      let needsRetry = await this.processEvents(chatId, messageId, stopTyping);
      while (needsRetry && retries < MAX_RETRIES) {
        retries++;
        console.log(`[gateway] retrying with fresh session (attempt ${retries}/${MAX_RETRIES})`);
        this.store.setAgentSessionId(undefined);
        await this.session?.close();
        this.session = null;
        await this.ensureSession();
        await this.requireSession().send(content, images.length > 0 ? images : undefined);
        needsRetry = await this.processEvents(chatId, undefined, stopTyping);
      }
      if (needsRetry) {
        const msgId = this.store.getState().currentMessageId;
        if (msgId) {
          const errorCard = buildDoneCard('Session failed after retries. Please try again.', [], 0, '0s', this.botName, '');
          await this.feishu.updateCard(msgId, errorCard);
        }
      }
    } catch (err) {
      console.error('[gateway] error:', err);
      await this.feishu.sendCard(chatId, buildNoticeCard('出错了', `\`${(err as Error).message}\``, 'red')).catch(() => {});
    } finally {
      stopTyping();
      this.processing = false;
      // Process next queued message
      this.drainQueue();
    }
  }

  /** Process the next message in the queue (if any) */
  private drainQueue(): void {
    if (this.processing || this.messageQueue.length === 0) return;

    const next = this.messageQueue.shift()!;
    // Skip stale messages (queued > 10 minutes)
    const age = Date.now() - next.enqueuedAt;
    if (age > 10 * 60 * 1000) {
      console.log(`[gateway] dropping stale queued message (age=${Math.round(age / 1000)}s)`);
      const expiredCard = buildNoticeCard('消息已过期', '排队时间过长，请重新发送。', 'grey');
      if (next.cardMsgId) {
        this.feishu.updateCard(next.cardMsgId, expiredCard).catch(() => {});
      } else {
        this.feishu.sendCard(next.chatId, expiredCard).catch(() => {});
      }
      // Try next
      this.drainQueue();
      return;
    }

    console.log(`[gateway] processing queued message (${this.messageQueue.length} remaining)`);
    this.processMessage(next.content, next.images, next.chatId, next.messageId, next.senderInfo, next.cardMsgId).catch((err) => {
      console.error('[gateway] queued message error:', err);
    });
  }

